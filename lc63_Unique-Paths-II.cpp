class Solution {
public:
    int uniquePathsWithObstacles(vector<vector<int>> &obstacleGrid)
    {
        int m = obstacleGrid.size();
        int n = obstacleGrid[0].size();
        // TODO: why int cant work
        vector<vector<long>> ans(m, vector<long>(n, 0));
        if (obstacleGrid[0][0] == 0) ans[0][0] = 1;

        for (int i = 1; i < m; ++i) {
            if (obstacleGrid[i][0] == 0) ans[i][0] = ans[i - 1][0];
        }
        for (int j = 1; j < n; ++j) {
            if (obstacleGrid[0][j] == 0) ans[0][j] = ans[0][j - 1];
        }
        for (auto i : ans) {
            for (auto j : i) {
                std::cout << j << " ";
            }
            std::cout << std::endl;
        }
        for (int i = 1; i < m; ++i) {
            for (int j = 1; j < n; ++j) {
                if (obstacleGrid[i][j] == 0)
                    ans[i][j] = ans[i - 1][j] + ans[i][j - 1];
            }
        }
        return ans[m - 1][n - 1];
    }
};

